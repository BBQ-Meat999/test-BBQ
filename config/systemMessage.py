"""
System prompts for every agent node.

各ノードのシステムプロンプトをここで一元管理する。
`AgentNode.__init__` が `SystemMessage.get(self.node_name)` を自動で呼ぶ。

各プロンプトはクラスメソッドの f 文字列 (Markdown 形式) で定義し、
実行時に設定値・共有ブロックを動的に注入できる。

【プロンプト設計の前提 — このシステム固有のコンテキスト経路】
  プロンプトは「モデルが実際に受け取る情報」を踏まえて書かれている。
  agents/utils/context_manager.py の挙動より:
    - ワーカーは互いのフルコードを見られない。注入されるのはファイル名+行数のみ
      (artifact_summary)。さらに Send API で並列実行されるため、初回は互いの
      成果物がそもそも存在しない。→ 整合性の唯一の正本は PM の agent_instructions。
    - work_plan はワーカーには先頭 500 字だけ注入される (truncate)。
      → 各ワーカーの詳細は agent_instructions[node] に入れる (こちらは非 truncate)。
    - CodeReview と Writer だけは _full_artifacts_message でフルコードを受け取る。
  この事実を各プロンプトが明示的に利用している。安易に簡略化しないこと。
"""

from __future__ import annotations

from config.settings import settings


